// =============================================================================
//   IMPORTS
// =============================================================================

import {Job} from "./Job";

// =============================================================================
//   INTERFACES
// =============================================================================

export interface JobConstructor
{
  processType?: string;
  new(id: string): Job;
}

// =============================================================================
//   VARIABLES
// =============================================================================

export const definitions: {[name: string]: JobConstructor} = {};

// =============================================================================
//   FUNCTIONS
// =============================================================================

export function JobDefinition(name: string)
{
  return function(constructor: JobConstructor)
  {
    definitions[name] = constructor;
    constructor.processType = name;
  };
}
