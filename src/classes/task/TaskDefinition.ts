// =============================================================================
//   IMPORTS
// =============================================================================

import {Task} from "./Task";

// =============================================================================
//   INTERFACES
// =============================================================================

export interface TaskConstructor<M extends TaskMemory, T extends Task<M>>
{
  taskType?: string;
  new(memory: M): T;
}

// =============================================================================
//   VARIABLES
// =============================================================================

export const taskList: {[name: string]: TaskConstructor<any, any>} = {};

// =============================================================================
//   FUNCTIONS
// =============================================================================

export function TaskDefinition(name: string)
{
  return function(task: TaskConstructor<any, any>)
  {
    taskList[name] = task;
    task.taskType = name;
  };
}
